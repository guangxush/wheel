package model;

/**
 * 存储的大数据对象
 *
 * @author: guangxush
 * @create: 2023/06/18
 */
public class BigDataModel {

    /**
     * 存储Key
     */
    private String key;

    /**
     * 存储Value
     */
    private String value;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
